/*
    int_categories.sql
    ------------------
    PURPOSE:
        dim_category is a dimension entity with its own surrogate key (category_sk). 
        This model creates that entity by joining the Portuguese category names 
        from products with their English translations.

        Note:
        - category_sk is a FK on dim_products, meaning products reference
          categories, not the other way around
        - A category can contain many products — it is a parent entity
        - Left join (instead of union) from products to stg_category
          since dim_category exists as a foreign key target for 
          dim_products. dim_category exists to describe the categories 
          that products belong to. A category that has no products is an 
          orphaned dimension record (it exists in the dimension but nothing 
          in your fact tables or other dimensions ever references it)

    DATA QUALITY NOTE:
        Products with null product_category_name are assigned a canonical 
        uncategorised label rather than being dropped or left null.

    KEY DECISIONS:
        - category_sk is generated by hashing the Portuguese category name,
          which is the natural key. This is stable: the same category name
          always produces the same surrogate key, meaning referential integrity
          is maintained even if the table is fully rebuilt.
        - DISTINCT ensures one row per unique category, since multiple products
          share the same category.
*/

with products as (

    select
        product_category_name
    from {{ ref('stg_olist_products') }}

),

translations as (

    select
        product_category_name,
        product_category_name_english
    from {{ ref('stg_product_category_name_translation') }}

),

-- Get all unique categories that actually appear on products
unique_categories as (

    select distinct
        product_category_name
    from products

),

joined as (

    select
        -- Stable surrogate key hashed from the natural key (Portuguese name)
        -- Null category gets a fixed label before hashing for consistency
        -- The `~` operator is Jinja's string concatenation
        {{ dbt_utils.generate_surrogate_key(
            ["coalesce(unique_categories.product_category_name, '" ~ var('uncategorised_label') ~ "')"]
        ) }}                                                        as category_sk,

        coalesce(
            unique_categories.product_category_name,
            '{{ var("uncategorised_label") }}'
        )                                                           as category_name,

        coalesce(
            translations.product_category_name_english,
            '{{ var("uncategorised_label") }}'
        )                                                           as category_name_english,

        -- metadata
        {{ metadata_columns() }}

    from unique_categories
    left join translations
        on unique_categories.product_category_name
            = translations.product_category_name

)

select * from joined